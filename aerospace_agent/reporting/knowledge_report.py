"""知识学习报告生成器 (Knowledge Learning Report Generator)。

:class:`KnowledgeReportGenerator` 把 :class:`KnowledgeGraph` (可选叠加
pipeline 处理报告) 转换为一份自包含的「航天知识学习报告」HTML,
辅助论文写作: 核心概念分析、知识空白识别、文献引用、公式速查、推导链路、
以及内嵌的交互式知识云图。

报告结构 (7 节)
---------------
1. 标题: 航天知识学习报告
2. 知识概览: 各类型节点数 + 知识图谱可视化
3. 概念网络分析: 核心概念 Top 10 / 聚类 / 知识空白
4. 文献学习记录 (若提供 pipeline_report): 已读文献 / 关键发现 / 研究趋势
5. 论文写作辅助: 引用列表 / 概念定义表 / 公式速查表 / 推导链路图
6. 知识云图嵌入 (iframe srcdoc, 自包含)
7. 下一步学习建议

风格: 深色专业航天工程报告, 内联 CSS, 与 report.py 一致 (紫色品牌色为主)。
"""

