# 节点类型中文名与颜色 (与 knowledge_cloud 保持一致)
_TYPE_INFO = {
    "concept": {"name": "概念", "color": "#4B3FE3"},
    "formula": {"name": "公式", "color": "#22A5F7"},
    "tool": {"name": "工具", "color": "#27D2BF"},
    "mission": {"name": "任务", "color": "#F87454"},
    "paper": {"name": "文献", "color": "#1DC981"},
}

# 关系中文名
_REL_NAMES = {
    "depends_on": "依赖",
    "used_by": "被使用",
    "alternative_to": "替代",
    "constraint_of": "约束",
}


class KnowledgeReportGenerator:
    """生成「航天知识学习报告」HTML, 辅助论文写作。"""

    DEFAULT_OUTPUT = "/workspace/reports/knowledge_learning_report.html"

    def __init__(self) -> None:
        # 延迟导入, 避免 rag <-> reporting 循环依赖
        self._cloud_gen = None

    @property
    def cloud_generator(self):
        """延迟获取 KnowledgeCloudGenerator 实例。"""
        if self._cloud_gen is None:
            from ..rag.knowledge_cloud import KnowledgeCloudGenerator
            self._cloud_gen = KnowledgeCloudGenerator()
        return self._cloud_gen

    # ==================================================================
    # 主入口
    # ==================================================================
    def generate(
        self,
        knowledge_graph: Any,
        pipeline_report: Optional[Union[dict, Any]] = None,
        output_path: str = DEFAULT_OUTPUT,
        embed_cloud: bool = True,
        cloud_output_path: Optional[str] = None,
    ) -> str:
        """生成知识学习报告 HTML, 返回文件绝对路径。

        参数
        -----
        knowledge_graph   : :class:`KnowledgeGraph`
        pipeline_report   : 文献处理报告 (可选), 可为 dict 或含 .result 的对象,
                            需含 papers / key_findings / trends 等字段
        output_path       : 报告 HTML 输出路径
        embed_cloud       : 是否内嵌知识云图 (iframe srcdoc, 自包含)
        cloud_output_path : 同时把云图存为独立文件的路径 (可选)
        """
        kg = knowledge_graph
        pr = self._normalize_pipeline_report(pipeline_report)

        # --- 生成知识云图 HTML (用于内嵌) ---
        cloud_html = ""
        cloud_iframe = ""
        if embed_cloud:
            # 始终通过 _build_html 获取 HTML 字符串 (generate 返回的是路径)
            cloud_html = self.cloud_generator._build_html(
                self.cloud_generator.export_graph_data(kg),
                "航天知识云图", snapshots=None,
            )
            # 若指定独立文件路径, 额外保存一份
            if cloud_output_path:
                os.makedirs(os.path.dirname(os.path.abspath(cloud_output_path)),
                            exist_ok=True)
                with open(cloud_output_path, "w", encoding="utf-8") as f:
                    f.write(cloud_html)
            # 用 iframe srcdoc 内嵌 (自包含, 允许加载 D3 CDN)
            cloud_iframe = self._build_cloud_iframe(cloud_html)
        elif cloud_output_path:
            self.cloud_generator.generate(kg, output_path=cloud_output_path)

        # --- 组装报告 ---
        parts: List[str] = []
        parts.append('<!DOCTYPE html><html lang="zh-CN"><head>')
        parts.append('<meta charset="UTF-8">')
        parts.append('<meta name="viewport" content="width=device-width, initial-scale=1.0">')
        parts.append(f"<title>航天知识学习报告</title>")
        parts.append(f"<style>{_REPORT_CSS}</style>")
        parts.append("</head><body><div class='container'>")

        parts.append(self._section_title(kg))
        parts.append(self._section_overview(kg))
        parts.append(self._section_concept_analysis(kg))
        if pr:
            parts.append(self._section_literature(kg, pr))
        parts.append(self._section_writing_aid(kg, pr))
        if cloud_iframe:
            parts.append(self._section_cloud(cloud_iframe))
        parts.append(self._section_next_steps(kg))

        now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
        parts.append(
            f"<footer>Generated by aerospace_agent.reporting.KnowledgeReportGenerator "
            f"&middot; {now} &middot; Self-contained HTML</footer>")
        parts.append("</div></body></html>")

        html_content = "\n".join(parts)
        os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(html_content)
        return os.path.abspath(output_path)

    # ==================================================================
    # 知识空白识别
    # ==================================================================
    def identify_knowledge_gaps(self, knowledge_graph: Any) -> List[str]:
        """识别知识空白, 返回描述字符串列表。

        识别规则:
        1. 度数为 0 的孤立节点 (完全无连接)
        2. concept 类型节点未连接到任何 paper 类型节点 (缺少文献支撑)
        3. concept 节点度数 = 1 (仅单一依赖, 知识链薄弱)
        """
        kg = knowledge_graph
        gaps: List[str] = []

        # 度数统计
        degree_map = self._degree_map(kg)

        # 规则 1: 孤立节点
        for nid, deg in degree_map.items():
            if deg == 0:
                n = kg.nodes[nid]
                type_name = _TYPE_INFO.get(n["type"], {}).get("name", n["type"])
                gaps.append(
                    f"孤立节点 [{type_name}] \"{nid}\": "
                    f"度数=0, 完全无连接。建议补充关联关系或移除。"
                )

        # 找到所有 paper 节点
        paper_ids = {nid for nid, n in kg.nodes.items() if n["type"] == "paper"}

        # 规则 2: concept 未连接到 paper (若有 paper 节点)
        if paper_ids:
            for nid, n in kg.nodes.items():
                if n["type"] != "concept":
                    continue
                # 检查是否通过任意路径连接到 paper 节点 (1-2 跳)
                reachable_papers = self._reachable_types(kg, nid, "paper", max_depth=2)
                if not reachable_papers:
                    gaps.append(
                        f"概念 \"{nid}\" 缺少文献支撑: "
                        f"在 2 跳内未连接到任何 paper 节点。"
                        f"建议补充相关文献并建立 used_by 关系。"
                    )

        # 规则 3: 度数=1 的 concept (知识链薄弱)
        for nid, deg in degree_map.items():
            if deg == 1 and kg.nodes[nid]["type"] == "concept":
                gaps.append(
                    f"概念 \"{nid}\" 知识链薄弱: 度数=1, "
                    f"仅单一关联。建议补充依赖/约束关系以丰富推导链。"
                )

        if not gaps:
            gaps.append("未发现明显知识空白, 知识图谱结构较为完整。")

        return gaps

    # ==================================================================
    # BibTeX 引用列表生成
    # ==================================================================
    def generate_citation_list(self, papers: Sequence[dict]) -> str:
        """从文献列表生成 BibTeX 格式引用字符串。

        每篇文献 dict 支持字段: title, authors/author, year, journal/venue,
        doi, url, type (默认 article)。
        """
        if not papers:
            return "% (无文献数据)"

        entries: List[str] = []
        for i, p in enumerate(papers):
            if not isinstance(p, dict):
                continue
            key = self._bibtex_key(p, i)
            etype = p.get("type", p.get("entry_type", "article"))
            lines = [f"@{etype}{{{key},"]

            title = p.get("title", "Untitled")
            lines.append(f"  title     = {{{title}}},")

            authors = p.get("authors") or p.get("author") or "Unknown"
            lines.append(f"  author    = {{{authors}}},")

            year = p.get("year", "")
            if year:
                lines.append(f"  year      = {{{year}}},")

            journal = p.get("journal") or p.get("venue") or p.get("publisher", "")
            if journal:
                lines.append(f"  journal   = {{{journal}}},")

            vol = p.get("volume", "")
            if vol:
                lines.append(f"  volume    = {{{vol}}},")

            pages = p.get("pages", "")
            if pages:
                lines.append(f"  pages     = {{{pages}}},")

            doi = p.get("doi", "")
            if doi:
                lines.append(f"  doi       = {{{doi}}},")

            url = p.get("url", "")
            if url:
                lines.append(f"  url       = {{{url}}},")

            # 去掉末尾逗号
            lines[-1] = lines[-1].rstrip(",")
            lines.append("}")
            entries.append("\n".join(lines))

        return "\n\n".join(entries)

    # ==================================================================
    # 内部: 图分析辅助
    # ==================================================================
    def _degree_map(self, kg: Any) -> Dict[str, int]:
        """计算每个节点的度数 (入度+出度)。"""
        deg: Dict[str, int] = defaultdict(int)
        for nid in kg.nodes:
            deg[nid] = 0
        for src, rels in kg.adj.items():
            for rel, lst in rels.items():
                for dst, w in lst:
                    deg[src] += 1
                    deg[dst] += 1
        return dict(deg)

    def _all_neighbors(self, kg: Any, nid: str) -> set:
        """获取节点的所有邻居 (无向, 合并入边与出边)。"""
        nbrs: set = set()
        for dst, rel, w in kg.neighbors(nid):
            nbrs.add(dst)
        # 入边 (radj[nid][relation] = [(src, weight)])
        for rel, lst in kg.radj.get(nid, {}).items():
            for src, w in lst:
                nbrs.add(src)
        return nbrs

    def _connected_components(self, kg: Any) -> List[List[str]]:
        """计算连通分量 (无向), 返回节点 id 列表的列表, 按大小降序。"""
        visited: set = set()
        components: List[List[str]] = []
        for start in kg.nodes:
            if start in visited:
                continue
            comp: List[str] = []
            q: deque = deque([start])
            visited.add(start)
            while q:
                cur = q.popleft()
                comp.append(cur)
                for nbr in self._all_neighbors(kg, cur):
                    if nbr not in visited:
                        visited.add(nbr)
                        q.append(nbr)
            components.append(comp)
        components.sort(key=len, reverse=True)
        return components

    def _core_concepts(self, kg: Any, top_n: int = 10) -> List[Tuple[str, int, str]]:
        """返回度数最高的 Top N 概念 [(id, degree, content)]。"""
        deg = self._degree_map(kg)
        concepts = [(nid, deg.get(nid, 0), kg.nodes[nid]["content"])
                    for nid, n in kg.nodes.items() if n["type"] == "concept"]
        concepts.sort(key=lambda x: x[1], reverse=True)
        return concepts[:top_n]

    def _reachable_types(
        self, kg: Any, start: str, target_type: str, max_depth: int = 2
    ) -> List[str]:
        """BFS 找到 start 在 max_depth 跳内可达的 target_type 节点 id 列表。"""
        if start not in kg.nodes:
            return []
        visited: set = {start}
        q: deque = deque([(start, 0)])
        found: List[str] = []
        while q:
            cur, d = q.popleft()
            if d >= max_depth:
                continue
            for nbr in self._all_neighbors(kg, cur):
                if nbr in visited:
                    continue
                visited.add(nbr)
                if nbr in kg.nodes and kg.nodes[nbr]["type"] == target_type:
                    found.append(nbr)
                q.append((nbr, d + 1))
        return found

    # ==================================================================
    # 内部: pipeline_report 规范化
    # ==================================================================
    def _normalize_pipeline_report(self, pr: Any) -> Optional[dict]:
        """把各种形式的 pipeline_report 统一为 dict。"""
        if pr is None:
            return None
        if hasattr(pr, "result"):
            pr = pr.result
        if isinstance(pr, dict):
            return pr
        if isinstance(pr, list):
            return {"papers": pr}
        return None

    # ==================================================================
    # 内部: BibTeX key 生成
    # ==================================================================
    @staticmethod
    def _bibtex_key(paper: dict, idx: int) -> str:
        """生成 BibTeX citation key: firstauthor + year + titleword。"""
        authors = paper.get("authors") or paper.get("author") or ""
        first_author = ""
        if authors:
            # 取第一个作者姓氏 (and / , 分隔)
            first = str(authors).split(" and ")[0].split(",")[0].split()[0]
            first_author = "".join(ch for ch in first if ch.isalpha())
        year = paper.get("year", "")
        title = paper.get("title", "")
        title_word = ""
        if title:
            for w in str(title).split():
                w = w.strip(".,;:!?\"'")
                if w and len(w) > 2 and w.lower() not in (
                    "the", "a", "an", "of", "for", "and", "in", "on", "to"):
                    title_word = "".join(ch for ch in w if ch.isalpha())
                    break
        parts = [p for p in (first_author.lower(), str(year), title_word.lower()) if p]
        return "_".join(parts) if parts else f"ref{idx + 1}"

    # ==================================================================
    # 内部: 知识云图 iframe
    # ==================================================================
    @staticmethod
    def _build_cloud_iframe(cloud_html: str) -> str:
        """把云图 HTML 包装为 iframe (srcdoc, 自包含)。"""
        # 转义 srcdoc 属性值中的引号与 &
        escaped = html.escape(cloud_html, quote=True)
        return (
            '<iframe class="cloud-iframe" srcdoc="' + escaped + '" '
            'style="width:100%;height:600px;border:1px solid #30363d;'
            'border-radius:10px;background:#0d1117;" '
            'loading="lazy" sandbox="allow-scripts allow-same-origin"></iframe>'
        )

    # ==================================================================
    # HTML 工具
    # ==================================================================
    @staticmethod
    def _table(
        data: Sequence[Sequence[Any]], headers: Sequence[str],
        num_cols: Optional[set] = None,
    ) -> str:
        num_cols = num_cols or set()
        th = "".join(
            f'<th class="{"num" if i in num_cols else ""}">'
            f'{html.escape(str(h))}</th>'
            for i, h in enumerate(headers))
        rows = []
        for row in data:
            cells = "".join(
                f'<td class="{"num" if i in num_cols else ""}">'
                f'{c if isinstance(c, str) and c.startswith("<") else html.escape(str(c))}</td>'
                for i, c in enumerate(row))
            rows.append(f"<tr>{cells}</tr>")
        return f"<table><thead><tr>{th}</tr></thead><tbody>{''.join(rows)}</tbody></table>"

